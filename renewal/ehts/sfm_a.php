    <td><p>SFM is  preferred where flexible series cables are desirable or when heated lengths  exceed the limitations of cut to length cables. <br>
      SFM series  resistance heating cable is functioned by the heat output from the core, circuit  lengths up to 12000 feet can be energized from a single power supply point.<br>
      The series  circuitry of SFM provides consistent watt-per-foot power output along the  entire length of the cable with no voltage drop.<br>
      SFM series  withstands the temperature exposure associated with steam purging. <br>
      Chromalox SFM  is light weight, easy to install and provides proven long term performance and  reliability.<br>
    SFM cables are  approved for use in ordinary and hazardous area.</p></td>
