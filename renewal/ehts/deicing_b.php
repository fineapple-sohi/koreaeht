    <td><p>Summary description of SEHMS</p>      </td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td>Pipeline electrical heating system in which unique technique is realized using skin-effect physical phenomenon (hereafter referred as SEHMS) is designed for heating of long and extra long pipelines 
      including buried ones as in the given project. SEHMS is the only industrial heating system being a costeffective method of heat tracing of long and extra long pipelines to ensure normal flow of the product.<br>
      In the SEHMS special heating elements are applied, which use SKIN-effect and proximity effect in ferromagnetic conductors at alternative power current.<br>
    The heating element is a low carbon steel tube with the wall thickness not less than 3mm, inside of which a non-magnetic conductor (copper) is placed. At the end of the run the conductor is connected to      a steel pipe, and at the beginning of the run alternating voltage is being applied between the pipe and the conductor. The voltage value is calculated taking into account the heat generation required and the    length of the heated segment.</td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td align="center"><img src="images/sect_img09.gif"></td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td>Alternative current runs through the whole inner conductor section. No significant surface effect arises      in nonmagnetic material with good conductivity. In the outer ferromagnetic conductor (steel pipe) the      SKIN-effect is clear cut and the whole current runs through the internal pipe layer with about 1mm      thickness. The potential of external surface of pipe keeps on being virtually zero. Due to light thickness      of the SKIN-layer, basic heat generating (up to 80%) is being performed in the steel pipe. At the      beginning and at the end of the circuit (heating pipe segment) the outer surface of the SKIN-tube is    reliably grounded what ensures additional safety of the SEHMS system.</td>
  </tr>
