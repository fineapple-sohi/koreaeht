    <td>1-2. Heat engineering calculations</td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td>According to BS 6351, calculation of heat loss from the pipeline surface is being performed      according to the following formula:<br>
      <br>
      P=(2&pi;&lambda;*&Delta;T&times;l)/ln(d2/d1)<br>
      <br>
      where:<br>
      P &ndash; power loss intensity, W,<br>
      &lambda; - effective heat conductivity factor, W/m&middot;&deg;K,<br>
      &Delta;T &ndash; temperature difference between the heated surface and the ambient air, K,<br>
      d2 &ndash; outer diameter of the thermal insulation, m,<br>
      d1 &ndash; inner diameter of the thermal insulation, m,<br>
    l &ndash; length, m.</td>
