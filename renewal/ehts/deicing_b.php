  <tr>
    <td>The current project includes the construction of a 6&rdquo; MNB pipeline, insulated with 10mm aerogel      (conductivity: 0.014 W/m&middot;oC, ambient temp 10oC) heated to a max temp. of 22 &deg;C. Temperature in      empty pipe or stagnant product shall be maintained at 20 &deg;C.<br>
      The heater element will be installed in one 27 mm carbon steel tube, strapped every 30 cm along      the length, located at 45o of the vertical at the bottom of the pipe.<br>
      The output heat in the heat tubes should make up 27 watts/meter for the section to maintain a      pipeline temp of 20 &deg;C.<br>
      The SKIN-EFFECT system will consist of one heating section. Power feeding will be carried out in      one place at the end of the pipeline.<br>
    Electric cubicles should be of the indoor metal enclosed and should contain all electrical protective,    indicating and operating devices including sequential control and fault indicators.</td>
