    <td>SEHMS advantages</td>
  </tr>
  <tr>
    <td>&nbsp;</td>
  </tr>
  <tr>
    <td>Minimum quantity of heating elements power supply points. According to the designing experience      of these systems, the maximum length of ONE LINE of SEHMS may be up to 10 000m. By boosting      two segments of the line from one point, the distance between power supply points may be up to      20 000m.<br>
      Less investment costs against other heat tracing systems. That is supported by the fact that in      SEHMS SKIN-cable of a big section is discharged from heat generating and in fact performs the      function of an integrated covering power network.<br>
      Electrical safety. The external surface of heating element (tube) has a zero potential against earth, it is      grounded and shields and protects completely the current-carrying conductor located inside.<br>
      Good thermal contact. Metal heat generating element (tube) is welded directly to the main pipeline or      is tightly attached to it by using special elements. For improved heat transfer a special heat conductive      paste is applied.<br>
    Reliability. Strong heat generating elements in form of steel tubes provide mechanical strength and      protection of current-carrying conductors from damage. Heat generating elements do not have outer    insulation, which can be damaged while installing.</td>
